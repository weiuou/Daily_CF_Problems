int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n;
    cin >> n;

    vector<int> nums(n);
    for (auto &x: nums)
        cin >> x;
    
    vector<int> tmp = nums;
    sort(tmp.begin(), tmp.end());

    int ans = 0;
    long long cur = 0;
    for (int i = 0; i < n; i ++) {
        cur += tmp[i] - nums[i];
        ans += (cur == 0);
    }

    cout << ans;

    return 0;
}