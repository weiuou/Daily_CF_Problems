int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int n;
    cin >> n;

    vector<int> nums(n);
    long long sum = 0;

    for (auto &x: nums)
        cin >> x, sum += x;

    int val = sum / n;

    long long cur = 0, ans = 0;

    for (auto &x: nums)
        cur += x - val, ans += abs(cur);
    
    cout << ans;

    return 0;
}